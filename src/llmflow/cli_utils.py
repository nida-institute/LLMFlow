"""CLI utility functions for LLMFlow project initialization."""
import logging
from pathlib import Path

logger = logging.getLogger(__name__)

HELLO_PROMPT = """---
system: |
  You are a friendly assistant who showcases multilingual greetings.
user: |
  Pick {{language_count}} languages randomly.
  For each one, identify the language in English, share a native-script greeting, and add a short cultural note.
  Respond as a markdown bullet list.
---
"""

HELLO_PIPELINE = """name: "Hello Multilingual Pipeline"
description: |
  Sample pipeline generated by `llmflow init`.
  Run it with:
    llmflow run --pipeline pipelines/hello-llmflow.yaml
variables:
  prompts_dir: "prompts"
  output_dir: "output"

llm_config:
  model: "gpt-4o-mini"
  temperature: 0.2
  max_tokens: 500

steps:
  - name: "multilingual-greeting"
    type: "llm"
    prompt:
      file: "hello.gpt"
      inputs:
        language_count: 5
    outputs: greeting
    saveas:
      path: "${output_dir}/hello.md"
"""


def init_project(base_dir: Path) -> None:
    """Initialize a new LLMFlow project structure."""
    prompts_dir = base_dir / "prompts"
    pipelines_dir = base_dir / "pipelines"
    output_dir = base_dir / "output"

    prompts_dir.mkdir(parents=True, exist_ok=True)
    pipelines_dir.mkdir(parents=True, exist_ok=True)
    output_dir.mkdir(parents=True, exist_ok=True)

    prompt_path = prompts_dir / "hello.gpt"
    pipeline_path = pipelines_dir / "hello-llmflow.yaml"

    if not prompt_path.exists():
        prompt_path.write_text(HELLO_PROMPT, encoding="utf-8")
        logger.info("Created prompts/hello.gpt")
    else:
        logger.info("prompts/hello.gpt already exists; leaving as-is.")

    if not pipeline_path.exists():
        pipeline_path.write_text(HELLO_PIPELINE, encoding="utf-8")
        logger.info("Created pipelines/hello-llmflow.yaml")
    else:
        logger.info("pipelines/hello-llmflow.yaml already exists; leaving as-is.")

    logger.info("Output directory ready at ./output")


def list_pipelines(directory: str) -> list[str]:
    """List all YAML pipeline files in a directory."""
    base = Path(directory)
    if not base.exists():
        return []
    pipelines: list[str] = []
    for pattern in ("*.yaml", "*.yml"):
        for path in base.rglob(pattern):
            if path.is_file():
                pipelines.append(str(path.relative_to(base)))
    return sorted(dict.fromkeys(pipelines))